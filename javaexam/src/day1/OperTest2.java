package day1;

public class OperTest2 {
	public static void main(String[] args) {
		int num =10;
		System.out.println(num);		// 10
		System.out.println(++num);		// 11
		System.out.println(++num);		// 12
		System.out.println(num++);		// 12
		System.out.println(num++);		// 13
		System.out.println(num);		// 14
	}
}
